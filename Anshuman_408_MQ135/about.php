<p>MQ-135 Gas sensor</p>

<br>
<h3>
The MQ-135 gas sensor senses the gases like ammonia nitrogen, oxygen, alcohols, aromatic compounds, sulfide and smoke. The operating voltage of this gas sensor is from 2.5V to 5.0V. MQ-135 gas sensor can be implementation to detect the smoke, benzene, steam and other harmful gases.</h3>
<h3>The earthâ€™s Atmospheric CO2 level is increasing day by day. The globalaverage atmospheric carbon dioxide in 2019 was 409.8 parts per million and inOctober-2020 it is 411.29. Carbon dioxide is a key greenhouse gas andresponsible for about three-quarters of emissions. So CO2 level monitoringhas also started to gain importance.</h3>
<h3>
In this project, we are going to use an MQ-135 sensor with Arduino to measure the CO2 concentration. The measured CO2 concentration values will be displayed on the OLED module and last we will also compare the Arduino MQ-135 sensor readings with Infrared CO2 sensor readings. Apart from CO2, we have also measured the concentration of LPG, Smoke, and Ammonia gas using Arduino.<h3></div>

<p >MQ-135 Sensor Features</p>
<br>
<h3>
Wide detecting scope.<br>
Fast response and High sensitivity.<br>
Stable and long life.<br>
Operating Voltage is +5V.<br>
Detect/Measure NH3, NOx, alcohol, Benzene, smoke, CO2, etc.<br>
Analog output voltage: 0V to 5V.<br>
Digital output voltage: 0V or 5V (TTL Logic).<br>
Preheat duration 20 seconds.<br>
Can be used as a Digital or analog sensor.<br>
The Sensitivity of Digital pin can be varied using the potentiometer.
</h3>

